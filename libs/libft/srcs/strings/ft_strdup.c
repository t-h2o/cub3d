#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		len;
	int		i;
	char	*dst;

	len = ft_strlen(str);
	dst = malloc(sizeof (char) * len + 1);
	if (!dst)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dst[i] = str[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
